package Les5.Oefening8;

public class Ofening2PremetiveFloat {
    public static void main(String[] args) {
        //Create a primitive floating point variable
        //Maak een primitieve drijvende-kommavariabele
        Float floatVal = 5.59837f;

        //Convert it to a string
        //Convert het naar een string
        String floatString = String.valueOf(floatVal);

        //Display the length of the string
        //Geeft de lengte van de string weer
        int stringLength = floatString.length();
        System.out.println("Geconverteerde string: "+floatString);
        System.out.println("Snaarlengte: " +stringLength);




    }
}
